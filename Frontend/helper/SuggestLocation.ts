export const SuggestLocation = (search: string): Array<string> => {
  const places: Array<string> = [];
  if (!search || search.trim() === "") {
    return [];
  }
  Object.keys(COUNTRIES).forEach((place) => {
    if (place.toUpperCase().trimStart().includes(search.toUpperCase().trimStart())) {
      places.push(place);
    }
  });
  return places;
};

export const getTimezones = (place: string): string[] => {
  // always take the country part before the dash
  const country = place.split(" - ")[0].trim();

  if (!COUNTRIES[country]) {
    return [];
  }

  const timezones = COUNTRIES[country];

  // normalize to array
  return Array.isArray(timezones) ? timezones : [timezones];
};
